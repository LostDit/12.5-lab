//Используется массив чисел с плавающей точкой для хранения данных.
//В коде не используются библиотеки, кроме <iostream>.
//Программа корректно использует обращения по индексам и выдаёт правильный ответ.
//Асимптотическая сложность алгоритма не более O(n ^ 2).
//Числа выводятся от большего к меньшему.
#include <iostream>

int main() {
    double numbers[15];

    // Ввод чисел пользователем
    std::cout << "Введите 15 дробных чисел: ";
    for (int i = 0; i < 15; ++i) {
        std::cin >> numbers[i];
    }

    // Сортировка массива методом пузырька (от большего к меньшему)
    for (int i = 0; i < 14; ++i) {
        for (int j = 0; j < 14 - i; ++j) {
            if (numbers[j] < numbers[j + 1]) {
                // Обмен значений
                double temp = numbers[j];
                numbers[j] = numbers[j + 1];
                numbers[j + 1] = temp;
            }
        }
    }

    // Вывод отсортированного массива
    std::cout << "Числа в порядке от большего к меньшему: ";
    for (int i = 0; i < 15; ++i) {
        std::cout << numbers[i] << " ";
    }
    std::cout << std::endl;

    return 0;
}